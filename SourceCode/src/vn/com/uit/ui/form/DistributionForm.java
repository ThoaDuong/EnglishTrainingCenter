    @Override
    public void onLoading() {

        tbClass.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseReleased(MouseEvent e) {
                int r = tbClass.rowAtPoint(e.getPoint());
                if (r >= 0 && r < tbClass.getRowCount()) {
                    tbClass.setRowSelectionInterval(r, r);
                } else {
                    tbClass.clearSelection();
                }

                int rowindex = tbClass.getSelectedRow();
                if (rowindex < 0) {
                    return;
                }
                if (e.isPopupTrigger() && e.getComponent() instanceof JTable) {
                    JPopupMenu popup = createPopUp();
                    popup.show(e.getComponent(), e.getX(), e.getY());
                }
            }
        });
    }

    private JPopupMenu createPopUp() {
        JPopupMenu result = new JPopupMenu();
        JMenuItem register = new JMenuItem("Thêm vào lớp");

        register.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                if (_traineeId != -1 && _courseId != -1) {
                    showConfirmDialog();
                } else {
                    JOptionPane.showMessageDialog(null, "Chưa chọn học viên", "Thông báo", JOptionPane.ERROR_MESSAGE);
                }
            }
        });

        result.add(register);

        return result;
    }

    private void showConfirmDialog() {
        int index = tbClass.getSelectedRow();
        TableModel model = tbClass.getModel();
        int classId = (int) model.getValueAt(index, 0);

        String msg = "Ghi danh học viên [ " + _traineeId + " ] vào lớp [ " + classId + " ] thuộc khoá học [ " + _courseId + " ] ? \n"
                + "Học phí thu tiền mặt: [ " + _fee.intValueExact() + " vnd ]";
        int dialogResult = JOptionPane.showConfirmDialog(this, msg, "Thông báo xác nhận", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
        if (dialogResult == JOptionPane.YES_OPTION) {
            _service.enrollClass(_courseId, classId, _traineeId);
        }
    }

    private void refreshTraineePanel() {
        lbTraineeName.setText("");
        lbTraineeAddress.setText("");
        lbTraineeBirthday.setText("");
        lbTraineeMark.setText("");

        DefaultTableModel _dm = (DefaultTableModel) tbCourseTaken.getModel();
        if (_dm.getRowCount() != 0) {
            _dm.getDataVector().removeAllElements();
            _dm.fireTableDataChanged();
        }
    }

    private void refreshClassPanel() {
        lbCourseName.setText("");

        DefaultTableModel _dm = (DefaultTableModel) tbClass.getModel();
        if (_dm.getRowCount() != 0) {
            _dm.getDataVector().removeAllElements();
            _dm.fireTableDataChanged();
        }
    }

    private void refreshUI() {
        refreshTraineePanel();
        refreshClassPanel();
        txtSearchClass.setText("");
        txtSearchTrainee.setText("");
        _traineeId = -1;
        _courseId = -1;
        _fee = null;
    }
