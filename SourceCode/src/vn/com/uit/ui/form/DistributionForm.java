    @Override
    public void displayTraineeInfo(TraineeDto trainee, ArrayList<ParticipationDto> listParticipations) {

        _traineeId = trainee.getId();

        refreshTraineePanel();

        lbTraineeName.setText(trainee.getName());
        lbTraineeAddress.setText(trainee.getAddress());
        lbTraineeBirthday.setText(trainee.getBirthday().toString());
        lbTraineeMark.setText(String.valueOf(trainee.getTestMark()));

        DefaultTableModel _dm = (DefaultTableModel) tbCourseTaken.getModel();

        if (listParticipations == null) {
            return;
        }

        Object rowData[] = new Object[8];
        for (int i = 0; i < listParticipations.size(); i++) {
            rowData[0] = listParticipations.get(i).getCourseId();
            rowData[1] = listParticipations.get(i).getCourseName();
            rowData[2] = listParticipations.get(i).getClassId();
            rowData[3] = listParticipations.get(i).getClassName();
            rowData[4] = listParticipations.get(i).getMark();

            _dm.addRow(rowData);
        }
    }

    @Override
    public void displayListClassInCourse(CourseDto course, ArrayList<ClassDto> list) {

        _courseId = course.getId();
        _fee = course.getFee();

        refreshClassPanel();

        lbCourseName.setText(course.getName());

        DefaultTableModel _dm = (DefaultTableModel) tbClass.getModel();

        Object rowData[] = new Object[5];
        for (int i = 0; i < list.size(); i++) {
            rowData[0] = list.get(i).getId();
            rowData[1] = list.get(i).getName();
            rowData[2] = list.get(i).getCurrentQuantity() + "/" + list.get(i).getCapacity();
            rowData[3] = list.get(i).getTime();
            rowData[4] = list.get(i).getStartedDate();

            _dm.addRow(rowData);
        }
    }

    @Override
    public void displayMessage(String msg, int flag) {
        JOptionPane.showMessageDialog(null, msg, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        if (flag == Constants.FLAG_REFRESH) {
            int index = tbClass.getSelectedRow();
            TableModel model = tbClass.getModel();
            int classId = (int) model.getValueAt(index, 0);
            _service.printReceipt(_courseId, classId, _traineeId, _staffId);
            refreshUI();
        }
    }

