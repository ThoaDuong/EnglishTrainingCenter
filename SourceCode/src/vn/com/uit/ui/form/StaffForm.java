    private JPopupMenu createPopUp() {
        JPopupMenu result = new JPopupMenu();
        JMenuItem edit = new JMenuItem("Sửa");
        JMenuItem delete = new JMenuItem("Xoá");

        edit.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                showDetailDialog();
            }
        });

        delete.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                int dialogResult = JOptionPane.showConfirmDialog(null, "Xoá nhân viên?", "Nhắc nhở", JOptionPane.YES_NO_OPTION);
                if (dialogResult == JOptionPane.YES_OPTION) {
                    int index = tbStaff.getSelectedRow();
                    TableModel model = tbStaff.getModel();
                    int staffId = (int) model.getValueAt(index, 0);
                    _service.deleteStaff(staffId);
                }
            }

        });

        result.add(edit);
        result.add(delete);

        return result;
    }

