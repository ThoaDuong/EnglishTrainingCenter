    @Override
    public void initialization() {
        _dm = (DefaultTableModel) tbStaff.getModel();
        _service = new StaffService(this);
    }

    @Override
    public void onLoading() {
        _service.loadListStaff();

        tbStaff.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseReleased(MouseEvent e) {
                int r = tbStaff.rowAtPoint(e.getPoint());
                if (r >= 0 && r < tbStaff.getRowCount()) {
                    tbStaff.setRowSelectionInterval(r, r);
                } else {
                    tbStaff.clearSelection();
                }

                int rowindex = tbStaff.getSelectedRow();
                if (rowindex < 0) {
                    return;
                }
                if (e.isPopupTrigger() && e.getComponent() instanceof JTable) {
                    JPopupMenu popup = createPopUp();
                    popup.show(e.getComponent(), e.getX(), e.getY());
                }
            }
        });
    }

    private void filter(String query) {
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<>(_dm);
        tbStaff.setRowSorter(tr);

        tr.setRowFilter(RowFilter.regexFilter("(?i)" + query));
    }

    private void showDetailDialog() {
        int index = tbStaff.getSelectedRow();
        TableModel model = tbStaff.getModel();

        StaffDto staff = new StaffDto();
        staff.setId((int) model.getValueAt(index, 0));
        staff.setName((String) model.getValueAt(index, 1));
        staff.setIdentityCardNumber((String) model.getValueAt(index, 2));
        staff.setPhoneNum((String) model.getValueAt(index, 3));
        staff.setSalary(BigDecimal.valueOf((new Integer((int) model.getValueAt(index, 4))).longValue()));
        staff.setStartedDate((Date) model.getValueAt(index, 5));
        staff.setRole((String) model.getValueAt(index, 6));

        JFrame main = (JFrame) SwingUtilities.getWindowAncestor(this);

        EditStaffForm dialog = new EditStaffForm(main, true, staff);
        int result = dialog.showDialog();

        if (result == Constants.STATUS_OK) {
            //reload
            _service.loadListStaff();
        }

    }
