    @Override
    public void onLoading() {
        txtName.setText(_staff.getName());
        txtIdCard.setText(_staff.getIdentityCardNumber());
        txtSalary.setText(String.valueOf(_staff.getSalary().intValueExact()));
        txtPhoneNum.setText(_staff.getPhoneNum());
        pickerStartedDate.setDate(_staff.getStartedDate());

        switch (_staff.getRole()) {
            case "Nhân viên quản lý":
                cbRole.setSelectedIndex(0);
                break;

            case "Nhân viên tài chính":
                cbRole.setSelectedIndex(1);
                break;

            case "Giảng viên":
                cbRole.setSelectedIndex(2);
                break;
        }
    }

    @Override
    public int showDialog() {
        setVisible(true);
        return Constants.STATUS_OK;
    }

    @Override
    public void displayMessage(int status, String msg) {
        if (status == Constants.STATUS_OK) {
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, msg, "Lỗi", JOptionPane.INFORMATION_MESSAGE);
        }
    }

    @Override
    public void initialization() {
        _service = new StaffService(this, Constants.MODE_MODIFY);
    }

    @Override
    public boolean validateInput() {
        if (txtName.getText().equals("") || !Utilities.isContainOnlyLetters(txtName.getText())) {
            JOptionPane.showMessageDialog(null, "Tên không hợp lệ!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (Utilities.isContainLetters(txtIdCard.getText()) || txtIdCard.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Số CMND không hơp lệ!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        if (!Utilities.isValidPhoneNumber(txtPhoneNum.getText()) || txtPhoneNum.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Số điện thoại không hơp lệ!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }

       try {
            if (new BigDecimal(txtSalary.getText()).compareTo(BigDecimal.ZERO) <= 0) {
                throw new Exception();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Mức lương không hợp lệ!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        return true;
    }
