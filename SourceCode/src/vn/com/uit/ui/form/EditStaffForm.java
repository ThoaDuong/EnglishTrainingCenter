    private void btnSaveChangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveChangeActionPerformed
        if (!validateInput()) {
            return;
        }
        StaffDto staff = new StaffDto();
        staff.setId(_staff.getId());
        staff.setName(txtName.getText());
        staff.setPhoneNum(txtPhoneNum.getText());
        staff.setIdentityCardNumber(txtIdCard.getText());
        staff.setSalary(new BigDecimal(txtSalary.getText()));
        switch (cbRole.getSelectedIndex()) {
            case 0:
                staff.setIdRole(Constants.ROLE_MANAGER);
                break;

            case 1:
                staff.setIdRole(Constants.ROLE_ACCOUNTANT);
                break;

            case 2:
                staff.setIdRole(Constants.ROLE_TEACHER);
                break;
        }

        _service.editStaff(staff);

    }//GEN-LAST:event_btnSaveChangeActionPerformed

